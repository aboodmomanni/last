#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	while (*s)
	{
		if (*s == (char)c)
		{
			return (s);
		}
		s++;
	}
	if ((char)c == '\0')
	{
		return (s);
	}
	return (NULL);
}
